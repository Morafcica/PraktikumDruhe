public class Posun {
    private Smer smer;
    private int oKolko;

    public Posun(Smer smer, int oKolko) {
        this.smer = smer;
        this.oKolko = oKolko;
    }

    public Smer getSmer() {
        return this.smer;
    }

    public int getoKolko() {
        return this.oKolko;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
